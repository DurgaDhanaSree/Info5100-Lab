    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:
        PatientClass newPatient = new PatientClass();

        newPatient.setName(this.jTextFieldName.getText());

        newPatient.setGender(this.jComboBoxGender.getSelectedItem().toString());

        newPatient.setLast_name(this.jTextFieldLname.getText());

        newPatient.setAge(Integer.parseInt(jTextFieldAge.getText()));

        newPatient.setEmail(this.jTextFieldEmail.getText());

        newPatient.setPatientType(this.PatientTypeJComboBox.getSelectedItem().toString());

        try {

            DatabaseConnector.updatepatient(selectedPatient, newPatient);

            System.out.println("The record has been changed successfully");

            populateTable();

        } catch (Exception ex) {

            System.out.println("Caught Successfully");

            System.out.println(ex);

        }

    }//GEN-LAST:event_jButtonSubmitActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = this.patientjTable.getSelectedRow();

        this.selectedPatient = this.patients.get(selectedRowIndex);

        populateData();

    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = this.patientjTable.getSelectedRow();

        try {

            this.selectedPatient = this.patients.get(selectedRowIndex);

            DatabaseConnector.deletePatient(selectedPatient);

            System.out.println("Patient deleted Successfully");

            populateTable();

        } catch (Exception ex) {

            System.out.println("Caught all Exceptions");

            System.out.println(ex);

        }

    }//GEN-LAST:event_deleteButtonActionPerformed

