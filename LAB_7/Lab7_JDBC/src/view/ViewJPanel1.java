    public void populateTable() {
        this.patients = DatabaseConnector.getPatients();

        DefaultTableModel model = (DefaultTableModel) this.patientjTable.getModel();

        model.setRowCount(0);

        for (PatientClass p1 : patients) {

            Object[] rows = new Object[7];

            rows[0] = p1.getId();

            rows[1] = p1.getName();
            rows[2] = p1.getLast_name();
            rows[3] = p1.getGender();

            rows[4] = p1.getPatientType();

            rows[5] = p1.getAge();

            rows[6] = p1.getEmail();

            model.addRow(rows);

        }

    }

